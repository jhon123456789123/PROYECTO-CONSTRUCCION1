

package semana2.practico_2ejercicio;


public class PersonaJuridica {

     
    private String nombre_Tenda;
    private String RUC;

    public PersonaJuridica(String nombre_Tenda, String RUC) {
        this.nombre_Tenda = nombre_Tenda;
        this.RUC = RUC;
    }

    public String getNombre_Tenda() {
        return nombre_Tenda;
    }

    public void setNombre_Tenda(String nombre_Tenda) {
        this.nombre_Tenda = nombre_Tenda;
    }

    public String getRUC() {
        return RUC;
    }

    public void setRUC(String RUC) {
        this.RUC = RUC;
    }

    
    
   
    
    
    
    
}
